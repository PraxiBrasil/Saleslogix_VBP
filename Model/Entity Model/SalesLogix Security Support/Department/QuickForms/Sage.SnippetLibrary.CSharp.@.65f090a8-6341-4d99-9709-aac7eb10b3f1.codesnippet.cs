			if(department != null)
			{
				IOwnerSecurityProfile securityProfile = department.Owner.GetOwnerSecurityProfile(department.Owner);
				if(securityProfile != null)
                    form.securityProfileLookup.LookupResultValue = securityProfile;
			}
        }
